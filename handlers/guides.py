def _menu_kb() -> InlineKeyboardMarkup:
    btns = [InlineKeyboardButton(text=g["label"], callback_data=f"g:{key}")
            for key, g in GUIDES.items()]
    rows = [btns[i:i + 2] for i in range(0, len(btns), 2)]
    return InlineKeyboardMarkup(inline_keyboard=rows)


def _guide_kb(key: str) -> InlineKeyboardMarkup:
    g = GUIDES[key]
    rows = [[InlineKeyboardButton(text="🤖 Задать вопрос по теме", callback_data=f"gask:{key}")]]
    spec = g.get("spec")
    if spec:
        rows.append([InlineKeyboardButton(text=spec[1], callback_data=f"fcat|{spec[0]}")])
    rows.append([InlineKeyboardButton(text="⬅️ К темам", callback_data="g:menu")])
    return InlineKeyboardMarkup(inline_keyboard=rows)


